def svg_to_icon(name: str, color_hex: str = "#ffffff", size: int = 24) -> QIcon:
    """
    Renders an inline SVG definition to a QIcon with the specified color and size.
    """
    svg_content = SVG_DEFINITIONS.get(name)
    if not svg_content:
        # Fallback empty QIcon
        return QIcon()
        
    # Replace default stroke/fill tags with explicit colors
    svg_colored = svg_content.replace('stroke="currentColor"', f'stroke="{color_hex}"')
    svg_colored = svg_colored.replace('fill="currentColor"', f'fill="{color_hex}"')
    
    renderer = QSvgRenderer(QByteArray(svg_colored.encode('utf-8')))
    if not renderer.isValid():
        return QIcon()
        
    pixmap = QPixmap(size, size)
    pixmap.fill(Qt.GlobalColor.transparent)
    
    painter = QPainter(pixmap)
    painter.setRenderHint(QPainter.RenderHint.Antialiasing)
    renderer.render(painter)
    painter.end()
    
    return QIcon(pixmap)


def get_icon(name: str, color_hex: str = "#ffffff", size: int = 24) -> QIcon:
    """Alias for svg_to_icon."""
    return svg_to_icon(name, color_hex, size)
